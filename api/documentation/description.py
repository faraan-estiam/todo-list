api_description = """
**todo-list-api** is a simple api to manage a todo list  
You can add or list all the tasks using the /todos endpoint  
You can edit a specific task by using the /todos/{id}  
---
**GET** /todos retrieves all tasks  
**POST** /todos creates a new task  
**GET** /todos/{id} retrieves an existing task  
**PUT** /todos/{id} updates an existing task  
**DELETE** /todos/{id} deletes an existing task  
"""